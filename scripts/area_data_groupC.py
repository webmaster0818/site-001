def city(name, slug, pref, geo_key, desc, subs, nearby_slugs):
    return {
     "level":"city","pref":pref,"pref_slug":"","city":name,
     "parent_slug":"","parent_name":pref,"geo":GEO[geo_key],
     "wards":[{"ward":name,"slug":slug,"desc":desc,"desc_short":desc,
               "subs":subs,"nearby":[(s,n) for s,n in nearby_slugs]}],
    }


def ward_group(pref, pref_slug, cityname, parent_slug, geo_key, wards):
    """政令市の区グループ（親=市ページ）。地理は市の geo_key を共通利用。"""
    c = {
     "pref":pref,"pref_slug":pref_slug,"city":cityname,
     "parent_slug":parent_slug,"parent_name":cityname,"geo":GEO[geo_key],
     "wards":[{"ward":w[0],"slug":w[1],"desc":w[2],"subs":w[3]} for w in wards],
    }
    order=[w["slug"] for w in c["wards"]]
    by={w["slug"]:f'{c["city"]}{w["ward"]}' for w in c["wards"]}
    for w in c["wards"]:
        w["desc_short"]=w["desc"]
        sibs=[s for s in order if s!=w["slug"]]
        w["nearby"]=[(c["parent_slug"],c["parent_name"])]+[(s,by[s]) for s in sibs]
    return c


