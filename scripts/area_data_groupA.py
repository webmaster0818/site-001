def build(city):
    """各wardにcity geoを付与し、nearby(親+兄弟3)を自動生成。"""
    wards = city["wards"]
    by = {w["slug"]: f'{city["city"]}{w["ward"]}' for w in wards}
    order = [w["slug"] for w in wards]
    for w in wards:
        w["geo"] = city["geo"]
        w["desc_short"] = w["desc"]
        sibs = [s for s in order if s != w["slug"]][:3]
        w["nearby"] = [(city["parent_slug"], city["parent_name"])] + [(s, by[s]) for s in sibs]
    return city

